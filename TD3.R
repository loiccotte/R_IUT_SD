pokemon$weight_kgNa=ifelse(is.na(pokemon$weight_kg),median(pokemon$weight_kg,na.rm=TRUE),pokemon$weight_kg)

pokemon$weight_kg=as.factor(pokemon$weight_kg)
pokemon$weight_height_mNa=ifelse(is.na(pokemon$height_m),median(pokemon$height_m,na.rm=TRUE),pokemon$height_m)
pokemon$height_mNa=as.factor(pokemon$height_m)
summary(pokemon$weight_kgNa)
summary(pokemon$weight_height_mNa)
